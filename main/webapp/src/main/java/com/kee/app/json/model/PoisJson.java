package com.kee.app.json.model;

public class PoisJson {
	
	private String _id;
	
	private String _name;
	
	private String _location;
	
	private String _address;
	
	private Integer star;
	
	private Integer rent;
	
	private String _createtime;
	
	private Integer _locstatus;

	public String get_id() {
		return _id;
	}

	public void set_id(String _id) {
		this._id = _id;
	}

	public String get_name() {
		return _name;
	}

	public void set_name(String _name) {
		this._name = _name;
	}

	public String get_location() {
		return _location;
	}

	public void set_location(String _location) {
		this._location = _location;
	}

	public String get_address() {
		return _address;
	}

	public void set_address(String _address) {
		this._address = _address;
	}

	public Integer getStar() {
		return star;
	}

	public void setStar(Integer star) {
		this.star = star;
	}

	public Integer getRent() {
		return rent;
	}

	public void setRent(Integer rent) {
		this.rent = rent;
	}

	public String get_createtime() {
		return _createtime;
	}

	public void set_createtime(String _createtime) {
		this._createtime = _createtime;
	}

	public Integer get_locstatus() {
		return _locstatus;
	}

	public void set_locstatus(Integer _locstatus) {
		this._locstatus = _locstatus;
	}
	
	

}
