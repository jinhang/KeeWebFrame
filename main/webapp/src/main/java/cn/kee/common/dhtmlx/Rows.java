package cn.kee.common.dhtmlx;

import java.util.ArrayList;
import java.util.List;

public class Rows {
	
	private String id;
	
	private ArrayList userdata;
	
	private List row;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public ArrayList getUserdata() {
		return userdata;
	}
	
	public void setUserdata(ArrayList userdata) {
		this.userdata = userdata;
	}

	public List getRow() {
		return row;
	}

	public void setRow(List row) {
		this.row = row;
	}



}
